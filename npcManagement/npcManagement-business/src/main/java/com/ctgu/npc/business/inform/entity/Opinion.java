package com.ctgu.npc.business.inform.entity;

public class Opinion {
	
	private String id;
	private String reportId;
	private String opinion;
	private String raiseId;
	private String raiseTime;
	private String checkId;
	private String checkTime;
	private String level;
	
	private String raiseName;
	private String checkName;
	private String title;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getReportId() {
		return reportId;
	}
	public void setReportId(String reportId) {
		this.reportId = reportId;
	}
	public String getOpinion() {
		return opinion;
	}
	public void setOpinion(String opinion) {
		this.opinion = opinion;
	}
	public String getRaiseId() {
		return raiseId;
	}
	public void setRaiseId(String raiseId) {
		this.raiseId = raiseId;
	}
	public String getRaiseTime() {
		return raiseTime;
	}
	public void setRaiseTime(String raiseTime) {
		this.raiseTime = raiseTime;
	}
	public String getCheckId() {
		return checkId;
	}
	public void setCheckId(String checkId) {
		this.checkId = checkId;
	}
	public String getCheckTime() {
		return checkTime;
	}
	public void setCheckTime(String checkTime) {
		this.checkTime = checkTime;
	}
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
	public String getRaiseName() {
		return raiseName;
	}
	public void setRaiseName(String raiseName) {
		this.raiseName = raiseName;
	}
	public String getCheckName() {
		return checkName;
	}
	public void setCheckName(String checkName) {
		this.checkName = checkName;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	

}
